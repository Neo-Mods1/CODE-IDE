package com.neo.ide.utils

import javax.lang.model.SourceVersion

object AndroidUtils {
    fun validatePackageName(packageName: String): String? {
        if (packageName.isBlank()) {
            return "Package name cannot be empty"
        }

        val parts = packageName.split(".")
        for (part in parts) {
            if (part.isEmpty()) {
                return "Package name cannot contain empty segments"
            }
            if (!SourceVersion.isIdentifier(part)) {
                return "Invalid package name segment: $part"
            }
            if (SourceVersion.isKeyword(part)) {
                return "Package name cannot contain keywords: $part"
            }
        }
        return null
    }
}
