package com.neo.ide.projects.util

import androidx.annotation.RestrictTo
import androidx.annotation.VisibleForTesting
import com.neo.ide.projects.IProjectManager
import com.neo.ide.projects.android.AndroidModule
import com.neo.ide.projects.android.androidAppProjects

/**
 * **For testing purposes only!**
 */
@RestrictTo(RestrictTo.Scope.LIBRARY_GROUP)
@VisibleForTesting
fun findAppModule(): AndroidModule? {
  return IProjectManager.getInstance().getWorkspace()?.androidAppProjects()
    ?.firstOrNull { it.path == ":app" }
}
